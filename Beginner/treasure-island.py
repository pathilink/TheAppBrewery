first_choice = input("Choose one direction: 'right' or 'left'?\n")
if first_choice.lower() == "left":
    sec_choice = input("Now you\'ve reached a river, would you like to swim or wait for a boat? Write 'swim' or 'wait':\n")
    if sec_choice.lower() == 'wait':
        thi_choice = input("After crossing the river you see 3 doors, which do you choose: 'red', 'blue' or 'yellow'?\n").lower()
        if thi_choice == "yellow":
            print("You Win!")
        elif thi_choice == "red":
            print("You burned by fire. Game Over!")
        elif thi_choice == "blue":
            print("You are eaten by beasts. Game Over!")
        else:
            print("Game Over!")
    else:
        print("You are attacked by trout. Game Over!")
else:
    print("You fall into a hole. Game Over!")